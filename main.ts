function columna_de_leds (temps: number) {
    led.plot(4, 1)
    led.plot(4, 4)
    led.plot(4, 0)
    led.plot(4, 2)
    led.plot(4, 3)
    basic.pause(temps)
    led.plot(3, 1)
    led.plot(3, 4)
    led.plot(3, 0)
    led.plot(3, 2)
    led.plot(3, 3)
    basic.pause(temps)
    led.plot(2, 1)
    led.plot(2, 4)
    led.plot(2, 0)
    led.plot(2, 2)
    led.plot(2, 3)
    basic.pause(temps)
    led.plot(1, 1)
    led.plot(1, 4)
    led.plot(1, 0)
    led.plot(1, 2)
    led.plot(1, 3)
    basic.pause(temps)
    led.plot(0, 1)
    led.plot(0, 4)
    led.plot(0, 0)
    led.plot(0, 2)
    led.plot(0, 3)
    basic.pause(1000)
    basic.clearScreen()
}
input.onButtonPressed(Button.A, function () {
    columna_de_leds(500)
})
